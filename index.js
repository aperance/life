function* gameGenerator(size, startingUniverse) {
  let currentUniverse = startingUniverse;
  while (true) {
    yield currentUniverse;

    let newUniverse = [];
    for (let row = 0; row < size; row++) {
      const rowPrev = row === 0 ? size - 1 : row - 1;
      const rowNext = row === size - 1 ? 0 : row + 1;

      for (let col = 0; col < size; col++) {
        const colPrev = col === 0 ? size - 1 : col - 1;
        const colNext = col === size - 1 ? 0 : col + 1;

        const neighborCount =
          currentUniverse[size * rowPrev + colPrev] +
          currentUniverse[size * rowPrev + col] +
          currentUniverse[size * rowPrev + colNext] +
          currentUniverse[size * row + colPrev] +
          currentUniverse[size * row + colNext] +
          currentUniverse[size * rowNext + colPrev] +
          currentUniverse[size * rowNext + col] +
          currentUniverse[size * rowNext + colNext];

        const index = row * size + col;

        switch (neighborCount) {
          case 2:
            newUniverse[index] = currentUniverse[index];
            break;
          case 3:
            newUniverse[index] = 1;
            break;
          default:
            newUniverse[index] = 0;
            break;
        }
      }
    }
    currentUniverse = newUniverse;
  }
}

function reshape(arr, size) {
  const oldArr = [...arr];
  const newArr = [];
  while (oldArr.length) newArr.push(oldArr.splice(0, size));
  return newArr;
}

